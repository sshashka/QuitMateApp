import SwiftUI


extension Color {
    static var buttonsPurpleColor: Color {
        return Color(ColorConstants.buttonsColor)
    }
    
    static var labelColor: Color {
        return Color(ColorConstants.labelColor)
    }
    
    static var backGroundColor: Color {
        return Color(ColorConstants.backgroundColor)
    }
    
    static var labelReversed: Color {
        return Color(ColorConstants.labelReversed)
    }
}
